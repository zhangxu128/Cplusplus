//给定一位研究者论文被引用次数的数组（被引用次数是非负整数）。编写一个方法，计算出研究者的 h 指数。
//
//h 指数的定义 : “h 代表“高引用次数”（high citations），一名科研人员的 h 指数是指他（她）的 
//（N 篇论文中）至多有 h 篇论文分别被引用了至少 h 次。（其余的 N - h 篇论文每篇被引用次数不多于 h 次。）”
//示例 :
//
//输入: citations = [3, 0, 6, 1, 5]
//	输出 : 3
//	解释 : 给定数组表示研究者总共有 5 篇论文，每篇论文相应的被引用了 3, 0, 6, 1, 5 次。
//由于研究者有 3 篇论文每篇至少被引用了 3 次，其余两篇论文每篇被引用不多于 3 次，所以她的 h 指数是 3。


#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;

class Solution {
public:
	int rec, i;
	int hIndex(vector<int>& citations) {
		sort(citations.begin(), citations.end(), greater<int>()); //贪心策略
		for (i = 0; i < citations.size(); i++) {
			if (citations[i] < i + 1) { //从大往小找
				rec = i; break;
			}
		}
		if (i == citations.size()) rec = citations.size();
		return rec;
	}
};

int main(void) {
	int n;
	cin >> n;
	vector<int> v(n);
	for (int i = 0; i < n; i++) {
		cin >> v[i];
	}
	Solution s;
	cout << s.hIndex(v) << endl;
	system("pause");
	return 0;
}