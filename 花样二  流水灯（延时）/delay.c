//函 数 名: Delay_MS(uint n)
//功能描述: 1ms延时
//输入参数: n
//输出参数:	无
//设 计 者：   
//创建日期：
//==================================================================
void Delay_MS(uint n)
{
					 
	int i=0,j=0;
	for(i=0;i<=n;i++)
	{
		for(j=0;j<=113;j++);	
	}
		
}
